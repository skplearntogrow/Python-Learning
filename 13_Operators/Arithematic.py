# Arthematic opeators are used with numeric values to perform common mathematical operations
x= 15
y= 4
print(x+y) # addition
print(x-y) # subtraction
print(x*y) # Multiplication
print(x/y) # division returns a quotient with decimal [float point number]
print(x%y) # Modulus gives quotient without decimal
print(x**y) # Exponentiation
print(x//y) # Floor Division returns reminder which is an integer
